package com.android.go4lunch.ui.notifications;

public class NotificationValueObject {

    private int id;

    private String text;

    public NotificationValueObject(int id, String text) {
        this.id = id;
        this.text = text;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
